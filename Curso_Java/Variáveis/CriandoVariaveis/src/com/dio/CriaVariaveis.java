package com.dio;

public class CriaVariaveis {

    public static void main(String[] args) {

        final int NUMERO_CONSTANTE = 15;
        //Constantes, representadas por "final" usam letras maiúsculas.
        int Idade = 18;
        String playerName2 = "Jorge";
        //Misturar letras e números não tem problema, e a notação camelo exige letra maiúscula nas palavras que não são a primeira.

        System.out.println(NUMERO_CONSTANTE);
        System.out.println(Idade);
        System.out.println(playerName2);
    }
}
